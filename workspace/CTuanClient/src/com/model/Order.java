package com.model;

public class Order implements java.io.Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int id_order;//订单id
	private int id_custom;//客户id
	private int id_cuisine;//菜式id
	private String businessname;//商家姓名
	private String name;//菜名
	private int num;//订餐数量
	private String time;//送达时间
	private String spot;//送达地点
	private String feedback;//用户评论
	private int status;//订单状态
	/*
	 * 0：待处理
	 * 1：待评价
	 * 2：被拒单
	 * 3：已处理
	 */
	public int getId_order() {//获取订单id
		return id_order;
	}
	public void setId_order(int id_order) {//设置订单id
		this.id_order = id_order;
	}
	public int getId_custom() {
		return id_custom;
	}
	public void setId_custom(int id_custom) {
		this.id_custom = id_custom;
	}
	public int getId_cuisine() {
		return id_cuisine;
	}
	public void setId_cuisine(int id_cuisine) {
		this.id_cuisine = id_cuisine;
	}
	public String getBusinessname() {
		return businessname;
	}
	public void setBusinessname(String businessname) {
		this.businessname = businessname;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getSpot() {
		return spot;
	}
	public void setSpot(String spot) {
		this.spot = spot;
	}
	public String getFeedback() {
		return feedback;
	}
	public void setFeedback(String feedback) {
		this.feedback = feedback;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
}
